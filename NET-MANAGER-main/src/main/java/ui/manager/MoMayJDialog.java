    @Override
    public void DongHo() {
        if (dongHoTimer == null || !dongHoTimer.isRunning()) {
            dongHoTimer = new Timer(1000, e -> {
                Date now = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
                lblGioBatDau.setText(sdf.format(now));
            });
            dongHoTimer.start();
        }
    }

    @Override
    public void TatDongHo() {
        if (dongHoTimer != null && dongHoTimer.isRunning()) {
            dongHoTimer.stop();
        }
    }

