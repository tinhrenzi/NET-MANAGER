    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblUsermager.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            String Vaitro = switch (item.getVaiTro()) {
                case 1 ->
                    "Quản lý";
                case 2 ->
                    "Nhân viên";
                default ->
                    "ko ro";
            };
            Object[] rowData = {
                item.getId(),
                item.getTen(),
                item.getMatKhau(),
                Vaitro,
                item.isTrangThai() ? "Hoạt động" : "Không hoạt động",
                item.getNamSinh(),
                item.getEmail(),
                item.getSoDienThoai(),
                item.getNgayTao(),
                item.getAnh()
            };
            model.addRow(rowData);
        });
    }

