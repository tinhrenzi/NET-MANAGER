            @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
        this.clear();
    }

    @Override
    public void setForm(DoUong entity) {
    System.out.println(" DonGia: " + entity.getDonGia());
    
    txtIdDU.setText(entity.getMaDoUong());
    txtName.setText(entity.getTenDoUong());
    txtDonGia.setText(String.valueOf(entity.getDonGia()));
    txtSoLuong.setText(String.valueOf(entity.getSoLuong()));
    }

   @Override
public DoUong getForm() {
    
    String ma = txtIdDU.getText().trim();
    String ten = txtName.getText().trim();
    String giaStr = txtDonGia.getText().trim();
    String soLuongStr = txtSoLuong.getText().trim();
    
    try {
        float gia = Float.parseFloat(giaStr);
        if (gia <= 0) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải lớn hơn 0!");
            return null;
        }

        int soLuong = Integer.parseInt(soLuongStr);
        if (soLuong < 0) {
            JOptionPane.showMessageDialog(this, "Số lượng không được âm!");
            return null;
        }

        return new DoUong(ma, ten, gia, soLuong);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Đơn giá và số lượng phải là số hợp lệ!");
        return null;
    }
}


    @Override
    public void fillToTable() {
         DefaultTableModel model = (DefaultTableModel) tblDU.getModel();
          model.setRowCount(0); // Clear dữ liệu cũ

    for (DoUong da : dao.findAll()) {
        Object[] row = {
           
            
            da.getMaDoUong(),
            da.getTenDoUong(),
            da.getDonGia(),
            da.getSoLuong()
        };
        model.addRow(row);
    }
    }

    public void edit() {
    int row = tblDU.getSelectedRow();
    if (row >= 0) {
        String ma = (String) tblDU.getValueAt(row, 0);
        DoUong da = dao.findByID(ma);
        if (da != null) {
           
            txtIdDU.setText(da.getMaDoUong());
             txtName.setText(da.getTenDoUong());
            txtDonGia.setText(String.valueOf(da.getDonGia()));
            txtSoLuong.setText(String.valueOf(da.getSoLuong()));
        }
    }
}


    @Override
    public void create() {
         DoUong du = getForm();
            dao.create(du);
fillToTable();
            clear();
    }

    @Override
    public void update() {
        DoUong da = getForm();
            dao.update(da);
            fillToTable();
            clear();
    }

    @Override
public void delete() {
    int row = tblDU.getSelectedRow();
    if (row >= 0) {
        String ma = tblDU.getValueAt(row, 1).toString(); // Lấy cột MaDoAn
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xoá?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            dao.deleteByID(ma);
            fillToTable(); 
            clear(); 
            JOptionPane.showMessageDialog(this, "Xoá thành công");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xoá");
    }
    fillToTable();
}

